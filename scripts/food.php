            <h1>4 benefits of keeping a food journal</h1>
            <p><b>1. It keeps you accountable for your calorie consumption.</b></p>
            <p>With how fast-paced the world has become, it's sometimes easy to fall into the trap of eating too much or
                too often if you're constantly on the go. Taking the time to consciously track what you're eating in a
                diary can help you be more mindful of what, how much, and how often you eat. This may help you have
                better willpower when it comes to resisting getting snacks too often or taking additional helpings,
                especially if – at least nutritionally-speaking – you really don't want or need them.</p>
            <p><b>2. It helps you zero-in on your nutritional needs.</b></p>
            <p>Equally as important as how much you eat is what you actually eat. A food diary can help you balance that
                out, too. As you write down the foods that you eat, you can organize them by food group (breads and
                starches, milk and dairy, fruits and vegetables, meat and poultry, etc.), key nutritional statistics
                (calories, fat, carbohydrates, fibre, sugar, etc.), or some other dietary system that takes a
                combination of these factors into account. That way, you can track what kinds of foods you need to eat
                more of, as well as which ones you should cut back on.</p>
            <p>For information on how to balance out the number and type of foods in each food group that you eat, visit
                the website for MyPlate, the United States Department of Agriculture's healthy eating initiative.</p>
            <p><b>3. It helps you identify your eating habits, and correct them if necessary.</b></p>
            <p>Another important consideration when making healthy food choices is the timing of when you eat. A food
                journal can help you notice patterns in the timing and frequency of when you chow down. For example,
                eating a small primary meal (breakfast, lunch, or dinner) or skipping it altogether may be the reason
                why you're constantly reaching for snacks throughout the day, or taking larger portions or extra
                helpings during another meal.</p>
            <p>Another possible reason why you may be over-eating – or eating certain kinds of food too often – is that
                you're being exposed to too many situations where you're either really happy and are eating to celebrate
                that, or you're depressed and are eating to cheer yourself up. Being under the influence of substances
                such as alcohol, marijuana, tobacco, or other drugs may also affect your eating habits.</p>
            <p><b>4. It works well together with other types of health diaries.</b></p>
            <p>Eating right is a key part of staying healthy, but it's not the only thing that you need to think about.
                Exercise, sleep, your mental state, and even sex, help to make up the bigger picture of your overall
                health. Tracking your eating habits alongside your other daily habits can help you put a regular routine
                together and plan out your day better. This will also help you identify areas of lacking or excess in
                your life, and figure out how to compensate for them. For example, you could work a little more exercise
                into your routine to make up for the fact that you had a big dinner at a restaurant or family
                celebration.</p>
            <p>As we noted, there are all kinds of different ways to keep track of your health in journals. Check out
                our article on health diaries for some examples.</p>
            <br>
            <h1>6 key things to keep track of in food diaries</h1>
            <br>
            <p><b>The food</b></p>

            <p>Obviously, this is the most important part. Write down everything that you eat or drink on a daily basis,
                no matter how large or small. Describe it in enough detail so that you understand exactly what you ate
                or drank; you may even want to go so far as to include a list of ingredients (if you know them).</p>
            <p><b>The amount</b></p>
            <p>Another key to eating right is watching how much you eat. General descriptions can be okay (e.g.
                small/medium/large, number of items), but if you're really trying to manage your portions, exact serving
                size information related to weight or volume is more useful. You may be able to get this information
                right on the recipe or food packaging, or you may need to measure it yourself using kitchen utensils.
            </p>
            <p><b>The nutrition stats</b></p>
            <p>A big part of watching what you eat and how much you eat is how much nutritional value you're getting out
                of it. Calories and fat are typical stats to record, but other ones may also be helpful to jot down,
                depending on your dietary needs. For example, if you're diabetic, you may want to watch your
                carbohydrate and sugar intake.</p>
            <p><b>The time</b></p>
            <p>The timing of when you eat food can be an important thing to write down in your journal, too. If you eat
                too little or too much at a certain time of day, you may end up trying to compensate by over-eating or
                under-eating later. Paying attention to when you eat – as well as how much you eat at the time – can
                help you identify unhealthy eating patterns and change them.</p>
            <p><b>The scenario</b></p>
            <p>Environmental factors can also play a part in what you eat or how much of it you eat. For example,
                consider answering some of these questions in your diary: were you eating at home, or somewhere else?
                Were you eating alone, or with other people? Were you focused on another activity while eating, or was
                eating your only preoccupation? Identifying circumstances in which you tend to over-eat (or under-eat)
                can help you be mindful of your eating habits in these situations, or perhaps even avoid them
                altogether.</p>
            <p><b>Your feelings</b></p>
            <p>Even your emotions can affect the amount and kind of food that you eat. Being overly happy and
                celebratory can cause you to over-eat, while being sad and depressed may cause you to under-eat (or even
                over-eat in an attempt to make yourself feel better). Consider writing down how hungry you are (on a
                scale of 1 to 10, for example) before you eat, and note how happy or sad you are before, while, and
                after you eat.</p>
            <br>
            <h1>4 tips for getting started with your food journaling</h1>
            <p><b>1. Decide what format of food journal is going to work for you.</b></p>
            <p>One of the first things you should do when starting a food diary is plan out what you're going to record, how you're going to record it, and why. For example, are you trying to lose weight, fight an eating disorder, or just general bad eating habits? Are you trying to monitor a specific nutrient to make sure that you aren't getting too little (or too much) of it? The reason you're writing your food journal will influence what you put in it.</p>
            <p>Also, think about how you're going to record things in your diary. Some people may prefer writing a free-form list, others may prefer to organize things in a table, while others may find it easier to take pictures of what they eat and then analyze it later (this is good for storing recipes, too!).</p>
            <p>If you're drawing a total blank, we have some more tips on how to start and write a diary.</p>
            <br>
            <p><b>2. Keep your diary handy.</b></p>
            <p>Take your diary with you wherever you go. If you have a digital/online one, consider taking notes on your smart phone, or download a diary-keeping app for it. Penzu's mobile app makes this extremely convenient, and you can get it for both Google Android devices and Apple iOS devices!</p>
            <p>Discipline yourself to record whatever you eat as soon as you start eating it; this saves you from having to remembering it later, which helps you stay accountable. Plus, it may make you think twice about eating something if you don't want to go to the trouble of recording it at that moment.</p>
            <br>
            <p><b>3. You're trying to help yourself, so be honest!</b></p>
            <p>While it may be tempting to not count certain things when submitting journal entries – such as condiments, drinks, or light snacks – it's not a good idea. Doing so may make you feel good in the short term, but in the long run, it will only frustrate you because you won't be able to see why you're not making progress. Remember, the more accurate your journal is, the better you'll be able to identify areas of your diet that need improving. Besides, nobody else is going to see your entries (unless you show it to them), so really, the only person who's judging you is you.</p>
            <p><b>4. Keep at it; it will get easier over time.</b></p>
            <p>Keeping a journal everything you eat may seem tough at first, with so much to keep track of so often. That's sort of the point, though: to get you to slow down and consciously think about what you eat, how much you eat, and when you eat. Eventually, though, it will likely become just another part of your routine. Part of the reason for this is that you will tend to eat similar types of foods from week to week (or even day-to-day), so you can just copy over the information for things that you've eaten previously whenever you eat them again.</p>
