            <p>Have you ever tried recording what you eat on a daily basis? How much of it you eat? When you eat it?
                You'd be surprised at what it can reveal about your diet and eating habits (or if you've tried, you'll
                know). If you're trying to lose weight, watch your nutrient intake, or just balance out the types of
                foods that you eat, a food diary can come in handy. It helps you be more conscious of how, what, and how
                much you eat, which can help you stay accountable when setting and working towards diet-related goals.
            </p>
            <br>
            <h1>So what exactly is a food diary?</h1>
            <p>A food diary is a record of foods that you eat on a meal-by-meal, daily, or weekly basis. You can include
                nutritional or other dietary information, organize and track foods by food group, and so on. This lets
                you monitor what, when, and how often you eat, as well as where your nutrition needs lie.</p>
