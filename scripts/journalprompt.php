                <h1>What is a Journal Prompt?</h1>
                <p>A journal prompt is a simple statement designed to inspire you or offer you an idea of what to write
                    about. It doesn't matter how much you love to write, or how often you write, there will be times when
                    you just don't know where to start. Prompts are also great for helping beginner writers get into journal
                    writing.</p>
    
                 <p>Basic journal prompts can be used by anyone to help get past a temporary block. The best journal prompts
                    inspire you to look deep inside yourself for inspiration, and to discover and capitalize on some of the
                    hidden thoughts and ideas you hadn't previously recognized or even considered. With practice, you can
                    develop the ability to find inspiration in everything you see. But, when you're just getting into
                    journal writing, or when you're really stuck, these suggested journal prompts can be very helpful.</p>
