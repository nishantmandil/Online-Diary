            <h1>Why Health Diaries are so Important?</h1>
            <p>If you are like most people, you probably cannot answer these questions because most of us do not store
                these details in our memories. We live day-to-day and never look back when it comes to understanding
                where our current state of health came from. However, looking back by using a health journal can reveal
                the truth about the good and bad of dietary choices, the amount of exercise you really get, why getting
                a good night’s sleep is so difficult, and stress levels. It can also reveal why efforts to address a
                host of health issues like obesity, high blood pressure, diabetes, and cardiovascular disease seem to
                fail.</p>
            <p>Whether it’s a food journal or a pregnancy journal, maintaining an online health diary as one file--to
                record and store important general health information over a time period to finally master good health
                and fitness, or to better manage a specific health issue--makes sense. People are creatures of habit,
                and breaking habits is difficult. The source of habits is found in the brain, which is the focus of
                cognitive-behavioral theory. It says that thoughts lead to feelings, which lead to behaviors. Identify
                the thoughts and you are now in control of the reactions!</p>
            <p>A health journal is not just a list of medical readings and calorie counts. It is a way to record
                feelings, goals, activities, surrounding events, and results for any particular area of health. Like any
                diary, it is private so the diarist can be honest about thoughts and the feelings. You can record
                personal physical information, motivational quotes, what others say about you, reactions, and behaviors
                related to health. It feels so good to be completely honest with yourself!</p>
            <br>
            <h1>Choose a Personal Diary Health Assistant</h1>
